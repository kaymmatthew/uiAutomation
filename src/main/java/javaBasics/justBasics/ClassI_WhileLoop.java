package javaBasics.justBasics;

public class ClassI_WhileLoop {

    public static void main(String[] args) {
        //while loop:

        int a = 1;
        while (a < 15) {
            System.out.println(a);
            a++;

        }

        //do while
        int k = 5;
        do{
            System.out.println(k);
            k++;
        }while (k<15);

    }
}
